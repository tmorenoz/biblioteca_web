                <p class="bold">Cookies</p>

                <p class="">Una cookie se refiere a un fichero que es enviado con la finalidad de solicitar permiso para
                  almacenarse en su ordenador, al aceptar dicho fichero se crea y la cookie sirve entonces para tener
                  información respecto al tráfico web, y también facilita las futuras visitas a una web recurrente. Otra
                  función que tienen las cookies es que con ellas las web pueden reconocerte individualmente y por tanto
                  brindarte el mejor servicio personalizado de su web.</p>

                <p class="">Nuestro sitio web emplea las cookies para poder identificar las páginas que son visitadas y
                  su frecuencia. Esta información es empleada únicamente para análisis estadístico y después la
                  información se elimina de forma permanente. Usted puede eliminar las cookies en cualquier momento
                  desde su ordenador. Sin embargo las cookies ayudan a proporcionar un mejor servicio de los sitios web,
                  estás no dan acceso a información de su ordenador ni de usted, a menos de que usted así lo quiera y la
                  proporcione directamente, visitas a una web . Usted puede aceptar o negar el uso de cookies, sin
                  embargo la mayoría de navegadores aceptan cookies automáticamente pues sirve para tener un mejor
                  servicio web. También usted puede cambiar la configuración de su ordenador para declinar las cookies.
                  Si se declinan es posible que no pueda utilizar algunos de nuestros servicios.</p>

                <p class="bold">Enlaces a Terceros</p>

                <p class="">Este sitio web pudiera contener en laces a otros sitios que pudieran ser de su interés. Una
                  vez que usted de clic en estos enlaces y abandone nuestra página, ya no tenemos control sobre al sitio
                  al que es redirigido y por lo tanto no somos responsables de los términos o privacidad ni de la
                  protección de sus datos en esos otros sitios terceros. Dichos sitios están sujetos a sus propias
                  políticas de privacidad por lo cual es recomendable que los consulte para confirmar que usted está de
                  acuerdo con estas.</p>

                <p class="bold">Control de su información personal</p>

                <p class="">En cualquier momento usted puede restringir la recopilación o el uso de la información
                  personal que es proporcionada a nuestro sitio web. Cada vez que se le solicite rellenar un formulario,
                  como el de alta de usuario, puede marcar o desmarcar la opción de recibir información por correo
                  electrónico. En caso de que haya marcado la opción de recibir nuestro boletín o publicidad usted puede
                  cancelarla en cualquier momento.</p>

                <p class="">Esta compañía no venderá, cederá ni distribuirá la información personal que es recopilada
